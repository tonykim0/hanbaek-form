/**
 * Claude에게 넘기는 계약서 분류 + 메타데이터 추출 프롬프트.
 * 모든 PDF를 한 번의 API 호출로 처리 (멀티-도큐먼트 vision).
 */
export function buildExtractionPrompt(fileNames: string[]): string {
  return `당신은 한백 EV 충전 인프라 사업의 계약서 분류 전문가입니다.
첨부된 PDF 파일들을 분석하여 아래 JSON 형식으로 정확하게 응답해주세요.
JSON 외의 텍스트, 마크다운 코드 블록은 출력하지 마세요.

## 분석 대상 파일 (${fileNames.length}개)
${fileNames.map((n, i) => `${i + 1}. ${n}`).join('\n')}

## 분류 카테고리 (반드시 아래 12개 중 하나)
1. 계약서
2. 합의서
3. 직인사용 동의서
4. 행위신고 업무대행 동의서
5. 전기차충전시설 설치신청서
6. 개인정보 동의서
7. 사전현장컨설팅 결과서
8. 입주자대표회의 회의록
9. 한전 전기요금 청구서
10. 건축물대장
11. 실사보고서
12. 기타 (위 11개에 해당하지 않을 때만 사용)

## 날짜 추출 기준 (YYYYMMDD 형식)
- 계약서 / 합의서: 계약일
- 직인사용 동의서 / 행위신고 업무대행 동의서: 문서 날짜
- 전기차충전시설 설치신청서: 신청일
- 개인정보 동의서: 동의일
- 사전현장컨설팅 결과서: 조사일
- 입주자대표회의 회의록: 회의일
- 한전 전기요금 청구서: 청구월 마지막 날 (예: 2025년 12월 청구서 → 20251231)
- 건축물대장: 발급일
- 실사보고서: 조사일
- 기타: 문서 내 가장 눈에 띄는 날짜, 없으면 오늘 날짜

## 허용 CPO 목록 (정확히 일치해야 함)
플러그링크, 대영채비, 신세계아이앤씨, 에버온, 파킹클라우드, 한화솔루션, LG U+, 현대엔지니어링, 한솔엠에스, SK일렉링크, 나이스인프라

## 허용 소재지 목록 (정확히 일치해야 함)
서울, 경기, 강원, 인천, 경북, 대구, 경남, 울산, 부산, 광주, 전남, 전북, 대전, 세종, 충남, 충북, 제주

## 허용 건축물 유형 (정확히 일치해야 함)
공동주택, 상업시설

## 허용 전력인입 옵션 (정확히 일치해야 함)
모자분리 + 한전수전, 한전수전, 모자분리

## 출력 형식 (JSON만 출력, 그 외 아무것도 출력하지 말 것)
{
  "현장명": "신안비치팔레스1차",
  "주소": "경기도 화성시 ...",
  "우편번호": "18469",
  "CPO": ["플러그링크"],
  "계약대수": 7,
  "총주차면수": 120,
  "건축물유형": "공동주택",
  "전력인입": "한전수전",
  "소재지": "경기",
  "현장담당자": "홍길동",
  "현장연락처": "031-123-4567",
  "설치위치": "지하 1층 주차장",
  "비고": "특이사항 없음",
  "files": [
    { "originalName": "원본파일명.pdf", "category": "계약서", "date": "20260410" },
    { "originalName": "회의록.pdf", "category": "입주자대표회의 회의록", "date": "20260331" }
  ],
  "confidence": {
    "현장명": 0.97,
    "계약대수": 0.90,
    "CPO": 0.95
  }
}

추출할 수 없는 필드는 null로 설정하세요.
CPO가 여러 개인 경우 배열에 모두 포함하세요.
confidence는 주요 필드(현장명, 계약대수, CPO 등)만 포함해도 됩니다.`;
}
